
# 将数字类型转换为字符串
num_str = str(11)
print(type(num_str), num_str)

float_str = str(11.345)
print(type(float_str), float_str)

# 字符串转数字
num = int("999")
print(type(num), num)
num2 = float("13.548")
print(type(num2), num2)

